#include "ft_printf.h"

void	ft_putstr_fd(char *s, int fd, size_t *len)
{
	int	i;

	if (!s)
		s = "(null)";
	i = 0;
	while (s[i] != '\0')
	{
		write (fd, &s[i], 1);
		i++;
		(*len)++;
	}
}
